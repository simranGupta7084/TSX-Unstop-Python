# Experiment with different arithmetic and logical operators in the 
# interpreter.

# Arithmetic Operators
print("Arithmetic Operators:")
print("10 + 5 =", 10 + 5)
print("9 // 2 =", 9 // 2)
print("2 ** 3 =", 2 ** 3)
print("9 % 2 =", 9 % 2)

# Comparison Operators
print("Comparison Operators:")
print("5 > 3:", 5 > 3)
print("5 == 5:", 5 == 5)
print("3 <= 2:", 3 <= 2)

# Logical Operators
print("Logical Operators:")
print("True and False:", True and False)
print("not True:", not True)
print("(5 > 2) and (3 < 1):", (5 > 2) and (3 < 1))